package com.online.shop.vo;

public class pageVO {

	int startrow;
	int endrow;
	
	public pageVO() {
		this.startrow = 0;
		this.endrow = 0;
	}

	public int getStartrow() {
		return startrow;
	}

	public void setStartrow(int startrow) {
		this.startrow = startrow;
	}

	public int getEndrow() {
		return endrow;
	}

	public void setEndrow(int endrow) {
		this.endrow = endrow;
	}
	
	
}
